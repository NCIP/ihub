package gov.nih.nci.ihub.writer.ncies.exception;

public class AuthenticationConfigurationException extends Exception {

	public AuthenticationConfigurationException() {
		super();
	}

	public AuthenticationConfigurationException(String message, Throwable cause) {
		super(message, cause);
	}

	public AuthenticationConfigurationException(String message) {
		super(message);
	}

	public AuthenticationConfigurationException(Throwable cause) {
		super(cause);
	}

}
